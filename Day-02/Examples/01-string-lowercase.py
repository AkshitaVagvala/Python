text = "Python is Awesome"
# Converting the string to lowercase using the lower() method
lowercase_text = text.lower()
print(lowercase_text)  # Output: python is awesome      
# Note: The lower() method does not modify the original string, it returns a new string with all characters converted to lowercase.
uppercase_text = text.upper()
print(uppercase_text)  # Output: PYTHON IS AWESOME
# Note: The upper() method does not modify the original string, it returns a new string
# with all characters converted to uppercase.

